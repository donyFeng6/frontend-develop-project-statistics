              <p class="js-cxda"><em>1</em>问：中国民用航空总局 (CAAC) 私用驾驶员执照（PPL）持有者能否在香港飞行？</p>
              <p class="even">答：可以。但请先完成一个check flight。</p>
              <p class="js-cxda"><em>2</em>问：持有有效的CAAC PPL可否在其它国家使用呢？</p>
              <p class="even">答：可以。中国是「国际民航组织」(ICAO)成员国。「中国民航总局私用驾驶员执照」(CAAC PPL)在所有成员国中都是认可的。但是，或许你被要求参加该国家的有关飞行法规及通讯用语的培训。</p>
              <p class="js-cxda"><em>3</em>如果我已持有有效的CAAC PPL，可否在其它国家申请正式转换执照呢？</p>
              <p class="even">答：当然可以。只要该国家都是「国际民航组织」成员国便可申请转换执照。不同国家有不同时间及条件要求。基本上，你须要进行恰当的程序，包括当地的飞行范围检视及认知该国的飞行法规。或许，你会被要求参加有关理论及飞行课程以符合单飞的资格。有关课程培训需时由2日至2-3个月不等。</p>
              <p class="js-cxda"><em>4</em>问：持有有效的PPL可否驾驶飞机飞越不同国家呢？</p>
              <p class="even">答：一般而言，你可以持有有效的「私用驾驶员执照」飞越大部份国家，但是你必须完成所有程序。首先，你必须先将飞机数据于你的目的地国家办妥登记手续。然后，飞行员必须预先获该国的入境批准，以旅客身份进入该国。</p>
              <p class="js-cxda"><em>5</em>问：考取的执照是否永远有效用呢？</p>
              <p class="even">答：是。执照按规定进行检查。</p>
              <p class="js-cxda"><em>6</em>问：飞行是否安全呢？</p>
              <p class="even">答：直升机必须有良好的保养，而且由合乎资格及态度严谨的直升机师驾驶，因此安全程度甚至比较其它交通工具更高。每次飞行前，飞行员都必定严谨有序地进行飞行前直升机安全检查。</p>
              <p class="js-cxda"><em>7</em>问：患有近视的人能否考取PPL？</p>
              <p class="even">答：可以，这情况会于驾驶员的体检合格证注明。</p>
              <p class="js-cxda"><em>8</em>问：我会否「晕机浪」呢？</p>
              <p class="even">答：每人体质不同。一般运动病（晕机病），最常见的症状是眩晕、浑身不舒服及焦躁不安。或许，你可参考以下的预防措施。飞行前切勿感到过饱或过饥。避免饮用含碳酸气的饮料。在飞行途中，你可尝试将不协调的视觉及平衡感觉调和，将视线向水平线远望便可缓和不适。慢慢地深呼吸。突然作太大的移动，会引致视觉及感觉不协调。如果还没有明显的改善，请不要介意使用呕吐袋。</p>
              <p class="js-cxda"><em>9</em>问：小型飞机能否在国际机场着陆？</p>
              <p class="even">答：商用客机都可以在国际机场着陆，其类型包括小型直升机。 </p>
              <p class="js-cxda"><em>10</em>问：当直升机引擎发生故障，直升机会否坠落？</p>
              <p class="even">答：不会。直升机可如扇或风车般运动。风车效应，亦即自转下滑，可让直升机在受操控之下下降及着陆。不会如砖块般下堕，反而似枫树种子般转动下降。即使在最恶劣情况下，富有丰富经验的飞行员仍可于一个比直升机面积稍大的范围内自转下滑，安然无恙地着陆。 </p>              
