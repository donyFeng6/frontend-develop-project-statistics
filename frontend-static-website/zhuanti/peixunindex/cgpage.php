            <h2 class="title" id="part2"><span><em>体检</em>要求</span></h2>
            <div class="con">
             <h3 class="slogan">飞行员条件自查：</h3>
             <p>1. 年满16周岁；</p>
             <p>2. 5年内无犯罪记录；</p>
             <p>3. 私用驾驶员需具有初中或者初中以上文化程度；</p>
             <p>商用驾驶员需具有高中或者高中以上文化程度；</p>
             <p>航线运输驾驶员需具有本科或者本科以上文化程度；</p>
             <p>4. 身心健康，无心理、精神障碍，无传染病、糖尿病、皮肤病、胃病、肾病、心脏病等，无癫痫、肺结核、结石、高血压等疾病。</p>
             <p>5. 四肢健康，无骨与关节疾病或畸形。</p>
             <p>6. 耳不背，口不吃。</p>
             <p>7. 无色盲色弱，任何一眼未矫正远视力不低于 0.1。</p>
             <p>8. 私用驾驶员要求每眼矫正或未矫正的远视力应当达到 0.5 或以上，双眼远视力应当达到 0.7 或以上（可佩戴眼镜）。每眼矫正或未矫正近视力在 30－50 厘米的距离范围内应当达到 0.5 或以上（可佩戴眼镜）。</p>
             <p>9. 商用驾驶员要求每眼矫正或未矫正的远视力应当达到0.7或以上，双眼远视力应当达到 1.0 或以上（可佩戴眼镜）。每眼矫正或未矫正的近视力在 30－50 厘米的距离范围内应当达到 0.5 或以上，在 100 厘米的距离应当达到 0.25 或以上（可佩戴眼镜）。</p>
             <p>10. 航线运输驾驶员要求任何一眼裸眼远视力应达到 0.7 或以上，双眼远视力应达到 1.0 或以上。 如任何一眼裸眼远视力低于 0.7，则必须同时满足下列条件：裸眼远视力不低于0.3；矫正视力不低于1.0（可佩戴眼镜）；屈光度不超过正负3.00D（球镜当量）。任何一眼裸眼近视力不应低于 1.0。如果做过角膜屈光手术，则一律当作视力不合格。</p>
             <p>11. 私用驾驶员与商用驾驶员对身高无具体要求，航线运输驾驶员要求男学员身高165-185厘米,女学员要求身高160-175厘米；体质指数（BMI）不应大于24或小于18.5。体质指数小于18.5，骨骼发育良好可视为合格；体质指数大于24，腰围小于85cm，腰臀比小于0.90，体型匀称，肌肉发达，胸、腹、腰等部位无脂肪堆积现象可视为合格。</p>
             <p><br/></p>
             <h3 class="slogan">体检依据：</h3>
             <p>私人及商业飞行员依据《中国民用航空人员医学标准和体检合格证管理规则（CCAR-67FS-R1）》和《民用航空人员体检鉴定和体检合格证管理程序（AP-67FS-002）》；航线飞行员依据《民用航空招收飞行学生体格检查鉴定规范（MH/T 7013-2006）》。</p>
             <p><br/></p>
             <p><span class="slogan">体检科目：</span>外科、内科、眼科、耳、鼻、喉科、血常规、脑电图、心电图、B超等。</p>
