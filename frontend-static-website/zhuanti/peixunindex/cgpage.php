            <h2 class="title" id="part3"><span><em>执照</em>介绍</span></h2>
            <div class="con">
              <p>飞行执照有<b>运动飞行</b>、<b>私人飞行</b>、<b>商用飞行</b>、<b>航线飞行</b>等不同类型。</p>
              <p><br/></p>
              <p><em>私人飞行执照（Private Pilot License，简称私照或PPL）</em>是每位职业飞行员飞行生涯的开始，也是普通民众为了娱乐目的最常考取的飞行执照，所以非常热门。私照一般需要训练50-60小时，全日制学习大约需要两到三个月时间。此种驾照的持有人只可进行不以盈利为目的的飞行。</p>
              <p><br/></p>
              <p><em>商用飞行执照（Commercial Pilot License，简称商照或CPL）</em>是飞行员职业道路的开端。除了要求更精确的操作，学习更高难度的机动动作之外，也有双发飞行训练（Multi-engine）。国内飞行学员在拿到商照之后还需要额外学习高性能飞机（High Performance），也就是驾驶喷气式小型商务飞机飞行20多小时后才算正式毕业。持有人可以担任小型喷气机机长和客运飞机副驾驶。</p>
              <p><br/></p>
              <p><em>航线运输驾驶员执照（Airline Transport Pilot License，简称航线照或ATPL）</em>是成为机长之前必须考取的执照。这一执照不但要求更高的理论知识，并且至少需要1500小时的飞行经验。持有人可以担任客运飞机机长。</p>
              <p><br/></p>
