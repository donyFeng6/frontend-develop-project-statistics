<script type="text/javascript">
	seajs.use(['JS/global.js'],function () {
		
		// 立即投资按钮事件
		$("#js-investment").on('click','.u-btntrs',function(){
			var sum = $(this).prevAll('.part').find('input');
			console.log(sum.val());
			if (!(/^([+-]?)\d*\.?\d+$/.test(sum.val()))) {
				sum.focus().val('').next('span').text('输入金额错误');
			}else{
				sum.next('span').text(' ');
			}
		});

		var prop = $("#js-prop");
		var sydj = $("#js-prop .sydj");  //使用卡卷
		var uncard = '<li>不使用卡卷</li>';
		// 选择使用或者不使用卡卷
		prop.find('input').live('change',function(){
			if($(this).val() == 1){
				sydj.addClass("act");
				sydj.find("ul").addClass("dj");
			}else{
				sydj.removeClass("act");
				prop.find('.property').hide();
				sydj.find("ul").html(uncard).removeClass('dj');
			}  		
		});
		// 点击弹出选择卡卷
		prop.on('click','ul.dj',function(e){
			$(this).addClass('act');
			$('#js-property').removeClass('u-hide');
		});

		// 点击卡卷关闭
		$('#js-property').on('click','.close',function(){
			sydj.find("ul").html(uncard).removeClass('act');
			$('#js-property').addClass('u-hide');
		});
		// 点击选择当前卡卷和取消选择卡卷
		$('#js-property li').toggle(function(){
			$(this).addClass('act');
		},function(){
			$(this).removeClass('act');
		});
		// 点击确定获取选择的卡卷
		$('#js-property').on('click','.confirm',function(){
			var secard = [];			
			var card = '';			
			$('#js-property .card').find('li').each(function(i,k){
				if ($(this).hasClass('act')) {
		            card += '<li>'+$(this).find('.type').html()+'<em>'+$(this).find('.cost').html()+'</em></li>';
		            secard[i] = $(this).data('id');
		        } 
			});
			sydj.find("ul").html(card).removeClass('act').next('input').val(secard);
			$('#js-property').addClass('u-hide');
		});
	});
</script>
