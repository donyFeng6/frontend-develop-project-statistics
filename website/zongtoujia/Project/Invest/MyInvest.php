  		*Countdown 倒计时函数
  		*deadline 截止时间 id显示的id
  		*/
  		function  Countdown(deadline,id){
		  	function getRTime(){ 
				var EndTime= new Date(deadline); //截止时间 
				var NowTime = new Date();
				if(EndTime.getTime() > NowTime.getTime()){
					console.log(EndTime.getTime()+"k"+NowTime.getTime());
					var t =EndTime.getTime() - NowTime.getTime(); 
					var d=Math.floor(t/1000/60/60/24); 
						t-=d*(1000*60*60*24); 
					var h=Math.floor(t/1000/60/60); 
						t-=h*60*60*1000; 
					var m=Math.floor(t/1000/60); 
						t-=m*60*1000; 
					var s=Math.floor(t/1000);
					var Timecon = d + "天"+h+"小时"+m+"分"+s+"秒";
						$(id).html(Timecon);					
				}else{
					// console.log('已过期');
				}				
			};
			setInterval(getRTime,1000);   			
  		}  		
