  <script type="text/javascript">
    $(function(){
      // 点击切换国内外学校
      $("#js-schoolxz").on("click","a",function(){
        $("#js-schoolxz").find("a").removeClass("active"); //删除类
        $(this).addClass("active");//给当前添加选中样式
        // 切换到相应的学校列表
        $("#js-flyschool").find(".fxpx-content").addClass("fxpx-hide").eq($(this).index()).removeClass("fxpx-hide");
      }); 

      // sliderInit();// 幻灯片 
      // 国内航校
      ajaxload("assets/inland.js",{city:"guonei"},"#js-inlanditem");
      // 国外航校
      ajaxload("assets/foreign.js",{city:"guowai"},"#js-foreignitem");

      // 幻灯片函数
      function sliderInit(){
        var slider = $("#js-slider"); //幻灯片
        var banner = slider.find(".banner").children(); //轮播图
        var carousel = slider.find(".carousel").children();  //列表显示
        var index = 0; //当前值
        var timer ; //定时器
        var time = 4000; //动画间隔时间
        // 侦听点击事件
        carousel.on("click",function(){
          index = $(this).index();
          lunbotu();
        });
        // 点击向左切换移动
        slider.on("click","a.left",function(){
          index--;
          lunbotu();
        });
        // 点击向右切换移动
        slider.on("click","a.right",function(){
          index++;
          lunbotu();
        });
        timer = setInterval(aototime,time); //设置设置定时器
        // 鼠标移入清除定时器，移出恢复定时器
        slider.hover(function(){
          clearInterval(timer);
        },function(){
          timer = setInterval(aototime,time);
        });
        // 定时器函数
        function aototime(){
          index++;
          lunbotu();
        };
        function lunbotu(){
          if (index > banner.length-1) {
            index = 0; 
          }else if (index < 0) {
            index = banner.length-1;
          };
          // 当前列表切换
          carousel.removeClass("now").eq(index).addClass("now");
          // 幻灯片播放
          banner.stop(true,false).fadeOut(500).removeClass("active").eq(index).fadeIn(500).addClass("active");
        }
      };
      // 列表数据请求函数 url请求地址 datum传参数 dom模板id
      function ajaxload(url,datum,dom){        
        $.ajax({
          type:"GET",
          url:url,
          dataType:"json",
          data:datum
        }).done(function(res){
          //使用baidu.template命名空间,输出HTML
          var html=baidu.template('itemPage',res);
          //渲染
          $(dom).find('.moreitem').before(html);
          // 如果学校个数为奇数显示期待更多航校
          if (res.records.length%2 != 0) {           
            $(dom).find('.moreitem').show();
          }else{
            $(dom).find('.moreitem').hide();
          };          
        }).error(function(res){
          console.log("error");
        });
      };

      
    });
  </script>
